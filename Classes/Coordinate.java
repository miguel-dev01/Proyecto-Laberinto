package Classes;

public class Coordinate {
	
	public int i;
	public int j;
	public int direction;
	
	public Coordinate () {} // Constructor vacio
	
	// Constructor clase Coordinate
	public Coordinate(int i, int j, int direction) { 
		
		this.i = i;
		this.j = j;
		this.direction = direction;
		
	}
	
	
} // cierre Clase Coordinate